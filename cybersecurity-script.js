// Cybersecurity specific animations
function initCybersecurityAnimations() {
    // Service cards staggered animation
    const serviceCards = document.querySelectorAll('.service-card');
    const serviceCardsObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry, index) => {
            if (entry.isIntersecting) {
                setTimeout(() => {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }, index * 200);
                serviceCardsObserver.unobserve(entry.target);
            }
        });
    }, { threshold: 0.3 });

    serviceCards.forEach(card => {
        card.style.opacity = '0';
        card.style.transform = 'translateY(30px)';
        card.style.transition = 'all 0.6s ease';
        serviceCardsObserver.observe(card);
    });

    // Stats cards animation
    const statCards = document.querySelectorAll('.stat-card');
    const statCardsObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry, index) => {
            if (entry.isIntersecting) {
                setTimeout(() => {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }, index * 150);
                statCardsObserver.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });

    statCards.forEach(card => {
        card.style.opacity = '0';
        card.style.transform = 'translateY(20px)';
        card.style.transition = 'all 0.5s ease';
        statCardsObserver.observe(card);
    });

    // Certification cards animation
    const certCards = document.querySelectorAll('.cert-card');
    const certCardsObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry, index) => {
            if (entry.isIntersecting) {
                setTimeout(() => {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateX(0)';
                }, index * 200);
                certCardsObserver.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });

    certCards.forEach(card => {
        card.style.opacity = '0';
        card.style.transform = 'translateX(-30px)';
        card.style.transition = 'all 0.5s ease';
        certCardsObserver.observe(card);
    });
}

// Initialize cybersecurity animations
document.addEventListener('DOMContentLoaded', () => {
    initCybersecurityAnimations();
});

